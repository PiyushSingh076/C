void delete_data(int emp[100][100],int id,int n){
    int i=0;
    while(emp[i][0]!=id){
        i++;
    }
    while(i<n-1){ // kyuki ek element toh kam ho gaya na ab thats why n-1
        emp[i][0]=emp[i+1][0];
        emp[i][1]=emp[i+1][1];
        i++;
    }
    
}
